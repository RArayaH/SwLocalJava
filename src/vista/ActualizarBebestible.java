    private void jB_BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_BuscarActionPerformed
        
        try {
            Bebestible objBeb = new Bebestible("","",this.jText_Codigo.getText(),1);
            objBeb.cargarCodigoProducto();
            if (Conexion.buscarProducto) {
                this.jText_MostrarNombre.setText(objBeb.getNombre());
                this.jText_MostrarVolumen.setText(objBeb.getVolumen());
                this.jText_MostrarPrecio.setText(String.valueOf(objBeb.getPrecio()));
            }
            else{
                JOptionPane.showMessageDialog(null, "Bebestible no existe", "Buscando", 2);
                this.jText_MostrarNombre.setText("");
                this.jText_MostrarVolumen.setText("");
                this.jText_MostrarPrecio.setText("");
                this.jText_Codigo.setText("");
                this.jText_Codigo.requestFocus();
            }
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jB_BuscarActionPerformed

