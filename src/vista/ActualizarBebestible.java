    private void jB_ActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_ActualizarActionPerformed
         try {
            Bebestible objBeb = new Bebestible("","",this.jText_Codigo.getText(),1);
            objBeb.cargarCodigoProducto();
            if (Conexion.buscarProducto) {
                
               objBeb.setNombre(this.jText_MostrarNombre.getText());
               objBeb.setVolumen(this.jText_MostrarVolumen.getText());
               objBeb.setPrecio(Integer.parseInt(this.jText_MostrarPrecio.getText()));
               objBeb.actualizarBebestible();
               JOptionPane.showMessageDialog(null, "Bebestible Actualizado", "Actualizar", 2);
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Bebestible no existe", "Buscando", 2);
                this.jText_MostrarNombre.setText("");
                this.jText_MostrarVolumen.setText("");
                this.jText_MostrarPrecio.setText("");
                this.jText_Codigo.setText("");
                this.jText_Codigo.requestFocus();
            }
            
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jB_ActualizarActionPerformed

    private void jB_EliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_EliminarActionPerformed
        
        try {
            Bebestible objBeb = new Bebestible("","",this.jText_Codigo.getText(),1);
            objBeb.cargarCodigoProducto();
            if (Conexion.buscarProducto) {
                
                int opcion =  JOptionPane.showConfirmDialog(null, "¿Deseas Eliminar Bebestible?","Eliminar",2);
                if (opcion == 0){
                    objBeb.eliminarBebestible();
                    JOptionPane.showMessageDialog(null, "Bebestible Eliminado", "Eliminar", 2);
                }
                
            }
            else{
                JOptionPane.showMessageDialog(null, "Bebestible no existe", "Buscando", 2);
                this.jText_MostrarNombre.setText("");
                this.jText_MostrarVolumen.setText("");
                this.jText_MostrarPrecio.setText("");
                this.jText_Codigo.setText("");
                this.jText_Codigo.requestFocus();
            }
            
            
        } catch (Exception e) {
        }
        
        
    }//GEN-LAST:event_jB_EliminarActionPerformed

