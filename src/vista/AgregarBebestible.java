    private void jB_AgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jB_AgregarActionPerformed
        try {
            Bebestible objBeb = new Bebestible(this.jText_Volumen.getText(),this.jText_Nombre.getText(),this.jText_Codigo.getText(),Integer.parseInt(this.jText_Precio.getText()));
            objBeb.cargarCodigoProducto();
            if (Conexion.buscarProducto) {
                System.out.println("El Bebestible ya existe");
                JOptionPane.showMessageDialog(null,"Bebestible ya existe!" ,"Guardar", 0);
                objBeb.imprimir();
            }
            else{
                objBeb.guardarBebestible();
                JOptionPane.showMessageDialog(null,"Datos almacenados" ,"Guardar", 1);
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jB_AgregarActionPerformed


